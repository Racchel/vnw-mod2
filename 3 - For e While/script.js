let contFor = 1, contWhile = 10

console.log("Criar um loop que conte de 1 até 10 usando FOR")
for(contFor; contFor <= 10; contFor++) {
   console.log(contFor)
}

console.log("Criar um loop que conte de 10 até 1 usando WHILE")
while(contWhile > 0) {
   console.log(contWhile)
   contWhile --
}

console.log("Criar um loop que conte todos os números ímpares de 1 até 100")
for(contFor = 1; contFor <= 100; contFor+=2) {
   console.log(contFor)
}

console.log("Criar um loop que conte todos os números pares de 1 até 100")
for(contFor = 0; contFor <= 100; contFor+=2) {
   console.log(contFor)
}
